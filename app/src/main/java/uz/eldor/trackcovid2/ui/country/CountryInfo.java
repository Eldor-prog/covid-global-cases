package uz.eldor.trackcovid2.ui.country;

public class CountryInfo{
	private String flag;
	private int id;
	private String iso2;
	private int lat;
	private int jsonMemberLong;
	private String iso3;

	public String getFlag(){
		return flag;
	}

	public int getId(){
		return id;
	}

	public String getIso2(){
		return iso2;
	}

	public int getLat(){
		return lat;
	}

	public int getJsonMemberLong(){
		return jsonMemberLong;
	}

	public String getIso3(){
		return iso3;
	}
}
